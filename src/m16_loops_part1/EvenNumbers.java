package m16_loops_part1;

public class EvenNumbers {
    public static void main(String[] args) {

        for (int i = 2; i <=10 ; i++) {
            if (i%2==0){
                System.out.print(i+" ");
            }
        }
        System.out.println();
        System.out.println("---------------------------");

        System.out.print(1);
        for (int i = 3; i <=19 ; i+=2) {
                System.out.print("-"+i);

            }






    }


}
